// @lc code=start
class Solution {
public:
    bool canPartition(vector<int>& nums) {
        int len = nums.size();

        // 求和
        int sum = 0;
        for (auto& n : nums) {
            sum += n;
        }
        // 判断奇偶数，如果是奇数，直接返回，
        if ((sum & 1) == 1) {
            return false;
        }

        int target = sum / 2;
        // 创建二维状态数组，行：物品索引，列：容量（包括0）
        std::vector<std::vector<bool>> dp(len, std::vector<bool>(target + 1, false));

        // 先填表格第0行，第1个数只能让容积为它子集的背包恰好装满
        if (nums[0] <= target) {
            dp[0][nums[0]] = true;
        }
        // 再填表格后面几行
        for (int i = 1; i < len; i++) {
            for (int j = 0; j <= target; j++) {
                // 直接从上一行先把结果抄下来，然后修正
                dp[i][j] = dp[i - 1][j];

                if (nums[i] == j) {
                    dp[i][j] = true;
                    continue;
                }
                if (nums[i] < j) {
                    dp[i][j] = dp[i - 1][j] || dp[i - 1][j - nums[i]];
                }
            }
        }

        return dp[len - 1][target];
    }
};
// @lc code=end

